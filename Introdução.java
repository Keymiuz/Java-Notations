
public class Introdução {
    public static void main(String[] args) {
        float num1 = 5.6f;
        int f =  (int)num1;

        System.out.println(f);
    }
}

// neste código o f é adicionado pois o java compila números sempre como double.
// quando você escreve um número decimal no código Java, como 5.6, 123.45, ou 0.001, o Java o interpreta automaticamente como um double por padrão.

